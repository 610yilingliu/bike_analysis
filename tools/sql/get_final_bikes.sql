	BIKE_ID, BIKE_LATITUDE, BIKE_LONGITUDE, FENCE_LATITUDE, FENCE_LONGITUDE, FENCE_LATITUDE_0, FENCE_LONGITUDE_0, FENCE_LATITUDE_1, FENCE_LONGITUDE_1, 
