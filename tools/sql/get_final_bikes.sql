	t1.FENCE_LATITUDE_0 = t2.LATITUDE_0 and t1.FENCE_LONGITUDE_0 = t2.LONGITUDE_0 and t1.FENCE_LATITUDE_1 = t2.LATITUDE_1 and t1.FENCE_LONGITUDE_1 = t2.LONGITUDE_1


